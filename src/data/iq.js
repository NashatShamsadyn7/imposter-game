//  بانکی پرسیارەکانی یاری IQ — چەند هاوپۆڵ، +١٠٠٠ پرسیار
//  پرسیارەکان بە عەرەبی، هەرکام لەگەڵ ٤ هەڵبژاردە و وەڵامی ڕاست.
//  بەشێک بە دەستی نووسراون، بەشێکیش بە جێنەرەیتەر دروست دەکرێن
//  (بیرکاری، زنجیرە، پایتەختەکان) بۆ گەیشتن بە +١٠٠٠.
// ═══════════════════════════════════════════════════════════

// ───── جێنەرەیتەری ڕێکوپێک (هەمیشە هەمان ئەنجام دەداتەوە) ─────
function mulberry32(seed) {
  let a = seed >>> 0
  return function () {
    a = (a + 0x6d2b79f5) | 0
    let t = Math.imul(a ^ (a >>> 15), 1 | a)
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296
  }
}
const rng = mulberry32(1337)
const randInt = (min, max) => Math.floor(rng() * (max - min + 1)) + min
const pick = (arr) => arr[Math.floor(rng() * arr.length)]
function shuffle(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1))
    ;[a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

let _id = 0
const nid = () => `q${++_id}`

// دروستکردنی پرسیاری ژمارەیی: وەڵامی ڕاست + ٣ هەڵبژاردەی هەڵە نزیک
function numMCQ(q, correct) {
  const set = new Set([correct])
  let guard = 0
  while (set.size < 4 && guard++ < 60) {
    const delta = randInt(1, Math.max(3, Math.round(Math.abs(correct) * 0.2) + 4))
    const cand = correct + (rng() < 0.5 ? -delta : delta)
    if (cand !== correct) set.add(cand)
  }
  // ئەگەر کەم بوو، بە هەژماری ساکار پڕی بکەرەوە
  let pad = 1
  while (set.size < 4) set.add(correct + (pad++) * 2 + 1)
  const choices = shuffle([...set]).map(String)
  return { id: nid(), q, choices, correct: choices.indexOf(String(correct)) }
}

// دروستکردنی پرسیاری هەڵبژاردنی دەقی
function textMCQ(q, correctText, distractors) {
  const choices = shuffle([correctText, ...distractors.slice(0, 3)])
  return { id: nid(), q, choices, correct: choices.indexOf(correctText) }
}

// ═══════════════════════════════════════════════════════════
//  ١) بیرکاری — بە جێنەرەیتەر (+٥٠٠ پرسیار)
// ═══════════════════════════════════════════════════════════
function genMath(count) {
  const out = []
  const seen = new Set()
  let guard = 0
  while (out.length < count && guard++ < count * 8) {
    const kind = randInt(0, 8)
    let q, ans
    if (kind === 0) {
      const a = randInt(8, 240), b = randInt(8, 240)
      q = `${a} + ${b} = ?`; ans = a + b
    } else if (kind === 1) {
      let a = randInt(20, 300), b = randInt(5, 280)
      if (b > a) [a, b] = [b, a]
      q = `${a} − ${b} = ?`; ans = a - b
    } else if (kind === 2) {
      const a = randInt(3, 19), b = randInt(3, 19)
      q = `${a} × ${b} = ?`; ans = a * b
    } else if (kind === 3) {
      const b = randInt(2, 15), ansv = randInt(2, 18)
      const a = b * ansv
      q = `${a} ÷ ${b} = ?`; ans = ansv
    } else if (kind === 4) {
      const p = pick([10, 20, 25, 40, 50, 75])
      const base = pick([40, 60, 80, 120, 160, 200, 240, 400])
      q = `كم يساوي ${p}% من ${base}؟`; ans = Math.round((p / 100) * base)
    } else if (kind === 5) {
      const a = randInt(2, 12)
      q = `ما هو مربّع العدد ${a}؟ (${a}²)`; ans = a * a
    } else if (kind === 6) {
      const a = randInt(10, 90), b = randInt(10, 90), c = randInt(10, 90)
      q = `${a} + ${b} + ${c} = ?`; ans = a + b + c
    } else if (kind === 7) {
      const a = randInt(6, 120)
      q = `ما هو ضعف العدد ${a}؟`; ans = a * 2
    } else {
      const a = randInt(2, 60) * 2
      q = `ما هو نصف العدد ${a}؟`; ans = a / 2
    }
    if (seen.has(q)) continue
    seen.add(q)
    const item = numMCQ(q, ans)
    item.cat = 'math'
    out.push(item)
  }
  return out
}

// ═══════════════════════════════════════════════════════════
//  ٢) زنجیرە و زیرەکی — بە جێنەرەیتەر (+٢٠٠ پرسیار)
// ═══════════════════════════════════════════════════════════
function genSequences(count) {
  const out = []
  const seen = new Set()
  let guard = 0
  while (out.length < count && guard++ < count * 6) {
    const kind = randInt(0, 2)
    let arr, ans
    if (kind === 0) {
      // زنجیرەی حیسابی
      const start = randInt(1, 20), step = randInt(2, 12)
      arr = [start, start + step, start + 2 * step, start + 3 * step]
      ans = start + 4 * step
    } else if (kind === 1) {
      // دووهێندەکراو
      const start = randInt(1, 6), r = randInt(2, 3)
      arr = [start, start * r, start * r * r, start * r * r * r]
      ans = start * r * r * r * r
    } else {
      // زیادبوونی پلەیی (+1, +2, +3 ...)
      const start = randInt(1, 10)
      let v = start, d = 1
      arr = [v]
      for (let i = 0; i < 3; i++) { v += d; d++; arr.push(v) }
      ans = v + d
    }
    const q = `أكمل المتتالية: ${arr.join('، ')}، ؟`
    if (seen.has(q)) continue
    seen.add(q)
    const item = numMCQ(q, ans)
    item.cat = 'iq'
    out.push(item)
  }
  return out
}

